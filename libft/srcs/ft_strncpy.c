#include <stdlib.h>

char	*ft_strncpy(char *dest, const char *src, size_t len)
{
	size_t i;

	i = 0;
	while (src[i] && i < len)
	{
		dest[i] = src[i];
		i++;
	}
	if (src[i] == '\0' && i < len)
		while (i < len)
		{
			dest[i] = '\0';
			i++;
		}
	return (dest);
}
